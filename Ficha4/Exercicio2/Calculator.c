long sum (int num1, int num2){
    return num1 + num2;  
}

long subt (int num1, int num2){
    return num1 - num2;
}

long multip (int num1, int num2){
    return num1 * num2;
}

long divis (int num1, int num2){
    return (float)num1 / num2;
}